    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
  
       DepartmentBean bean = new DepartmentBean();
       bean.setName(deptName.getText());
       bean.setCode(deptCode.getText());
       bean.setEmail(deptEmail.getText());
       bean.setPhone(deptPhone.getText());
       FacultyBean selectedFaculty = (FacultyBean) facultySelect.getSelectedItem();
       bean.setFacultyId(selectedFaculty.getId());
       
        try{
            DBManager.addDepartment(bean);
            getDepartment();
            clear();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_addButtonActionPerformed

