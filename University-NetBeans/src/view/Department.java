    private void departmentListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_departmentListValueChanged
       
       DepartmentBean bean = departmentList.getSelectedValue();
       if(bean != null){
       deptId.setText(String.valueOf(bean.getId()));
       deptName.setText(bean.getName());
       deptCode.setText(bean.getCode());
       deptEmail.setText(bean.getEmail());
       deptPhone.setText(bean.getPhone());
       }
    }//GEN-LAST:event_departmentListValueChanged

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try{
            DepartmentBean bean = departmentList.getSelectedValue();
            
            bean.setName(deptName.getText());
            bean.setCode(deptCode.getText());
            bean.setEmail(deptEmail.getText());
            bean.setPhone(deptPhone.getText());
            DBManager.updateDepartment(bean);
            getDepartment();
            clear(); 
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try{
            DepartmentBean bean = departmentList.getSelectedValue();
            DBManager.delDepartment(bean.getId());
            getDepartment();
            clear();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

