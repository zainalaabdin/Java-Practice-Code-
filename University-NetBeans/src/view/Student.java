    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        StudentBean bean = new StudentBean();
        bean.setName(stdName.getText());
        bean.setRollNum(rollNum.getText());
        bean.setEmail(email.getText());
        bean.setPhone(phone.getText());
        bean.setGender(gender.getText());
        bean.setDateOfBirth(dob.getText());
        bean.setAddress(address.getText());
        bean.setAdmission(admissionDate.getText()); 
        bean.setRemarks(remarks.getText());
        BatchBean selectedBatch = (BatchBean) batchSelect.getSelectedItem();
        bean.setBatchId(selectedBatch.getId());
        try{
            DBManager.addStudent(bean);
            getStudent();
            clear();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_addButtonActionPerformed

