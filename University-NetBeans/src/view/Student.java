    public void getFaculty(){
       try{
           Vector<FacultyBean> faculty = DBManager.getFaculty();
           for(FacultyBean bean: faculty){
               facultySelect.addItem(bean);
           }
       }catch(Exception e){
           e.printStackTrace();
       }
   
   }
   
   public void getDepartment(){
       try{
           deptSelect.removeAllItems();
           FacultyBean bean = (FacultyBean) facultySelect.getSelectedItem();
           Vector<DepartmentBean> department = DBManager.getDepartment(bean.getId());
           for(DepartmentBean dept: department ){
           deptSelect.addItem(dept);
           }
       }catch(Exception e){
           e.printStackTrace();
       }
   }
   
   public void getBatch(){
       try{
           batchSelect.removeAllItems();
           DepartmentBean bean = (DepartmentBean) deptSelect.getSelectedItem();
           if(bean != null){
           Vector<BatchBean> batch = DBManager.getBatch(bean.getId());
           for(BatchBean bat: batch ){
           batchSelect.addItem(bat);
           }
           }
       }catch(Exception e){
           e.printStackTrace();
       }
   }
   
   
   public void getStudent(){
       try{
          BatchBean bean = (BatchBean) batchSelect.getSelectedItem();
          if(bean != null){
          Vector<StudentBean> std = DBManager.getStudent(bean.getId());
          studentList.setListData(std);
          }
        }catch(Exception e){
            e.printStackTrace();
        }
   }
